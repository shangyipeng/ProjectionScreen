package com.example.myapplication.bean;

public class WeekDay {
    /** 星期的显示名称*/
    public String week;
    /** 对应的日期*/
    public String day;

    public String getWeek() {
        return week;
    }

    public void setWeek(String week) {
        this.week = week;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }
}
